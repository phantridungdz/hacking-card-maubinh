type CardSet = 'set1' | 'set2';

const getCardImageUrl = (cardNumber: number, cardSet: CardSet): string => {
  const suits = ['spades', 'clubs', 'diamonds', 'hearts'];
  const ranks = [
    'ace',
    '2',
    '3',
    '4',
    '5',
    '6',
    '7',
    '8',
    '9',
    '10',
    'jack',
    'queen',
    'king',
  ];

  if (cardNumber < 0 || cardNumber > 51) {
    throw new Error('Card number must be between 0 and 51.');
  }

  const suitIndex = cardNumber % 4;
  const rankIndex = Math.floor(cardNumber / 4);
  const key =
    `${ranks[rankIndex]}_of_${suits[suitIndex]}` as keyof typeof cardImagesSet1;
  return cardSets[cardSet][key];
};

function shuffle(array: any[]) {
  for (let i = array.length - 1; i > 0; i--) {
    const j = Math.floor(Math.random() * (i + 1));
    [array[i], array[j]] = [array[j], array[i]];
  }
  return array;
}

